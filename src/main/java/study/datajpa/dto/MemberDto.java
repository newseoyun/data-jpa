package study.datajpa.dto;

import lombok.Data;

@Data // 웬만하면 Data 노노
public class MemberDto {

    private Long id;
    private String username;
    private String teamName;

    public MemberDto(Long id, String username, String teamName) {
        this.id = id;
        this.username = username;
        this.teamName = teamName;
    }
}
